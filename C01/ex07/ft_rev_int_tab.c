void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	tmp;

	i = 0;
	while (i < size)
	{
		j = 0;
		while (j < (size - 1) - i)
		{
			tmp = tab[j + 1];
			tab[j + 1] = tab[j];
			tab[j] = tmp;
			j++;
		}
		i++;
	}
}
