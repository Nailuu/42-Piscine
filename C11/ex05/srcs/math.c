int	ft_add(int a, int b)
{
	return (a + b);
}

int	ft_sub(int a, int b)
{
	return (a - b);
}

int	ft_divide(int a, int b)
{
	return (a / b);
}

int	ft_multiplication(int a, int b)
{
	return (a * b);
}

int	ft_modulo(int a, int b)
{
	return (a % b);
}
